    private static Map<Integer, ResponseStatusEnum> codeToResponseStatusMapping;

    private ResponseStatusEnum(int responseStatusCode) {
        this.responseStatusCode = responseStatusCode;
        this.responseStatus = name();
    }

    public static ResponseStatusEnum getResponseStatus(int i) {
        if (codeToResponseStatusMapping == null) {
            initMapping();
        }
        return codeToResponseStatusMapping.get(i);
    }

    private static void initMapping() {
        codeToResponseStatusMapping = new HashMap<Integer, ResponseStatusEnum>();
        for (ResponseStatusEnum m : values()) {
            codeToResponseStatusMapping.put(m.responseStatusCode, m);
        }
    }

    public String getResponseStatusCode() {
        return String.valueOf(responseStatusCode);
    }

